/*
  Tipagem dinâmica

  Tipos de dados:
    # string - palavra ou caractere
    # number - inteiro e decimal
    # boolean - verdadeiro ou falso (true ou false)
    # object - objeto
    # undefined - null

  Tipos de variaveis:
    # var - não é obrigatório inicializar
    # let - é obrigatório inicializar
    # const - não permite alterar o tipo após ser inicializado
*/

// Atribuição é sempre com o '='
// = -> recebe
// == -> igual


// Operadores Aritméticos
//     - Adição (+)
var soma = 5 + 2
console.log(soma)
//     - subtração (-)
let sub = 5 - 2
console.log(sub)
//     - Multiplicação (*)
let mult = 5 * 2
console.log(mult)
//     - Divisão (/)
let div = 5 / 2
console.log(div)
//     - Módulo (%)
let mod = 6 % 2
console.log(mod)
